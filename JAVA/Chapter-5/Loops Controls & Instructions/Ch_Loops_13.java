// Print the sum of the first 20 natural numbers.

public class Ch_Loops_13 {
    public static void main(String[] args) {
        int sum = 0;
        for (int a = 1; a <= 20; a++) {
            sum = sum + a;
        }
        System.out.println(" The sum of first 20 natural numbers is : " + sum);
    }

}
