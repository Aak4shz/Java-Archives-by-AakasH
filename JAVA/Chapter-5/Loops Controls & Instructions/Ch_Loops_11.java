// Using For loops in java :-

public class Ch_Loops_11 {
    public static void main(String[] args) {
        for (int a = 0; a <= 10; a++) { // Initialization, Condition, Increment.
            System.out.println(a);
        }
    }
}


// For loops :- for loops are used when the number of iterations is known beforehand.
// It consists of three main parts: initialization, condition, and increment/decrement.