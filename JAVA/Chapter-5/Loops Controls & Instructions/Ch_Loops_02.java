// Using While loops in java :-
public class Ch_Loops_02 {
    public static void main(String[] args) {

        // Initialization → starting value of i is 1
        int i = 1;

        // Condition → loop will run till i is less than or equal to 10
        while (i <= 10) {

            // Print statement with value of i
            System.out.println("Hello World " + i);
            i++; // Increment → increase i by 1 each time
        }        // loop ends when i becomes 11

        System.out.println("End the loop !"); // loop end message .
    }
}
