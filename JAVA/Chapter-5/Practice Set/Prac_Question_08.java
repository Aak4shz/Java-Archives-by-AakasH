// Print sum of first 50 natural numbers using for loop.

public class Prac_Question_08 {
    public static void main(String[] args) {

        int sum = 0;
        for (int i = 1; i <= 50; i++) {
            sum += i;
        }
        System.out.println("Sum of the first 50 natural numbers is : " + sum);

    }

}
