// Demonstration of instance and static variables in Java.

public class Data_Types_01 {

    public static void main(String[] args) {

        String name = "Sachin ChutMarike"; // instance variable
        int RollNo = 69; // instance variable
        String CollegeName = "St.Xavier's College Ranchi."; // static variable

        System.out.println("My name is : " + name);
        System.out.println("Roll no is : " + RollNo);
        System.out.println("And My College is : " + CollegeName);

    }
}
