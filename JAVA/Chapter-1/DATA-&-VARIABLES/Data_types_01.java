// Demonstration of instance and static variables in Java.

public class Data_types_01 {

    public static void main(String[] args) {

        String name = "Rahul"; // instance variable
        int RollNo = 69; // instance variable
        String SchoolName = "St.Xavier School Bundu."; // static variable

        System.out.println("My name is : " + name);
        System.out.println("Roll no is : " + RollNo);
        System.out.println("And My School is : " + SchoolName);

    }
}
