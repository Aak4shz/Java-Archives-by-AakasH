
// Write a java program to add Two numbers and Three numbers, Four Numbers.

public class Prac_Qna_03 {
    public static void main(String[] args) {
        int a = 55;
        int b = 45; 
        int sum = a + b;
        System.out.println("The Sum of Two Numbers is : " + sum);

        int x = 283;
        int y = 234;
        int z = 736;
        int sum1 = x+y+z;
        System.out.println("The Sum of Three Numbers is : " + sum1);

        int m = 597;
        int n = 546;
        int o = 257;
        int p = 675;
        int sum2 = m+n+o+p;
        System.out.println("The Sum of Four numbers is : " + sum2);


    }
}

// Output:
// The Sum of Two Numbers is : 100
// The Sum of Three Numbers is : 1253
// The Sum of Four numbers is : 2075