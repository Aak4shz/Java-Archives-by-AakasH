// Ternary operators Statement
public class Ch_Conditionals_09 {
    public static void main(String[] args) {
        int number = 10;
        String result = number % 2 == 0 ? "Even" : "Odd";
        System.out.println(result);
    }
}

/*
 * Ternary operators Statement ->
 * A Ternary operator evaluates the test conditions and executes a block of code,
 * based on the result of the condition.
 * This is the Syntax of Ternary operators Statement -> condition ? expression1 : expression2 ;
 */