// Write a java Program to find a character in a String.
public class Question_04 {
    public static void main(String[] args) {
        String word = "Say My Name ! You Damn Right .";
        int index = word.indexOf('y');
        int symbol = word.indexOf('!');
        System.out.println("Index of y is : " + index);
        System.out.println("Index of ! is : " + symbol);
        
    }
}
